    //Muestra por defecto la consulta de deudores
    private void mostrar() throws Exception {
        String sql;
        DefaultTableModel Modelo = new DefaultTableModel();
        Modelo.addColumn("SUGERIDO POR:");
        Modelo.addColumn("TITULO");
        Modelo.addColumn("AUTOR");
        Modelo.addColumn("EDITORIAL");
        Modelo.addColumn("EDICION");
        Modelo.addColumn("ID");
        jTable1.setModel(Modelo);
        sql = "SELECT Nombres, Apellidos, Titulo, Autor, Editorial, Edicion, IdSugerencia FROM Sugerencia INNER JOIN Personas WHERE idPersona = IdPersonas;";
        String[] datos = new String[6];
        try {
            Statement st = cn.getConnection().createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString(1) + " " + rs.getString(2);
                datos[1] = rs.getString(3);
                datos[2] = rs.getString(4);
                datos[3] = rs.getString(5);
                datos[4] = rs.getString(6);
                datos[5] = rs.getString(7);
                Modelo.addRow(datos);

            }
            jTable1.setModel(Modelo);
        } catch (SQLException ex) {
            System.out.println("Error al mostrar:" + ex.getMessage());
        }
    }

