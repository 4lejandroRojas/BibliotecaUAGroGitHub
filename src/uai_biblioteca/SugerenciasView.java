    private void jMEliminarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMEliminarMousePressed
        String ID = "";
        try {
            int fila = jTable1.getSelectedRow();
            if (fila >= 0 && jCBTitulo1.isSelected() == false) {
                ID = (jTable1.getValueAt(fila, 5).toString());
            }
        } catch (Exception e) {
            System.out.println("Error al dar clic derecho en fila " + e.getMessage());
        }

        try {
            String sql = "DELETE FROM Sugerencia WHERE idSugerencia ='" + ID + "';";
            Statement st = cn.getConnection().createStatement();
            st.executeUpdate(sql);
            st.close();
            mostrar();

        } catch (Exception e) {
            System.out.println("Error al eliminar: " + e.getMessage());
        }
    }//GEN-LAST:event_jMEliminarMousePressed

