    private void jTable1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MousePressed
        try {
            int fila = jTable1.getSelectedRow();
            if (fila >= 0 && jCBTitulo1.isSelected() == false) {
                tFTitulo1.setText(jTable1.getValueAt(fila, 1).toString());
                tFAutor1.setText(jTable1.getValueAt(fila, 2).toString());
                tFEditorial1.setText(jTable1.getValueAt(fila, 3).toString());
                tFEdicion1.setText(jTable1.getValueAt(fila, 4).toString());
                tFISBN1.setText("N/A");

            } else {
                tFTitulo2.setText(jTable1.getValueAt(fila, 1).toString());
                tFAutor2.setText(jTable1.getValueAt(fila, 2).toString());
                tFEditorial2.setText(jTable1.getValueAt(fila, 3).toString());
                tFEdicion2.setText(jTable1.getValueAt(fila, 4).toString());
                tFISBN2.setText("N/A");
            }
        } catch (Exception e) {
            System.out.println("Error al seleccionar fila " + e.getMessage());
        }
    }//GEN-LAST:event_jTable1MousePressed

