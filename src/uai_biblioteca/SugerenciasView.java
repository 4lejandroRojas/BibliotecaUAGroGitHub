//Imprime reporte de sugerencia de donacion de titulación
    @SuppressWarnings("unchecked")
    void ReporteSugerencia(String Matricula, int Responsable) {
        try {
            String ruta = "src/reportes/SugerenciaBiblio.jasper";
            Map parametros = new HashMap();
            parametros.put("AUT1", String.valueOf(tFAutor1.getText()));
            parametros.put("TIT1", String.valueOf(tFTitulo1.getText()));
            parametros.put("EDI1", String.valueOf(tFEdicion1.getText()));
            parametros.put("EDITO1", String.valueOf(tFEditorial1.getText()));
            parametros.put("ISBN1", String.valueOf(tFISBN1.getText()));

            parametros.put("AUT2", String.valueOf(tFAutor2.getText()));
            parametros.put("TIT2", String.valueOf(tFTitulo2.getText()));
            parametros.put("EDI2", String.valueOf(tFEdicion2.getText()));
            parametros.put("EDITO2", String.valueOf(tFEditorial2.getText()));
            parametros.put("ISBN2", String.valueOf(tFISBN2.getText()));

            parametros.put("valormatricula", Matricula);
            parametros.put("Responsable", Responsable);

            JasperReport jr = (JasperReport) JRLoader.loadObjectFromFile(ruta);
            JasperPrint jp = JasperFillManager.fillReport(jr, parametros, cn.getConnection());
            JasperViewer jv = new JasperViewer(jp, false);
            jv.setVisible(true);
            jv.setTitle("Sugerencia de donación de libros para titulación");

        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }

